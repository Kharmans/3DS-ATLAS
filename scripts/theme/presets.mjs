/**
 * Build the CSS variable name for a color key.
 * @param {string} key  Color key
 * @returns {string}
 */
export function cssVar(key) {
  return `--${CSS_NAMESPACE}-${key.replace(/[A-Z]/g, (c) => `-${c.toLowerCase()}`)}`;
}

/**
 * Generate the derived CSS variables from a base palette.
 * @param {Object<string, string>} colors  Base palette keyed by semantic color name.
 * @returns {Object<string, string>}
 */
export function generateDerivedColors(colors) {
  const derived = {};
  const ns = CSS_NAMESPACE;
  const { Color } = foundry.utils;
  const white = Color.fromString('#ffffff');
  const lighten = (hex, percent) => Color.fromString(hex).mix(white, percent / 100).css;
  if (colors.today) {
    const today = Color.fromString(colors.today);
    derived[`--${ns}-highlight-bg`] = today.toRGBA(0.2);
    derived[`--${ns}-current-hour`] = today.toRGBA(0.12);
  }
  if (colors.primary) {
    derived[`--${ns}-selected-bg`] = Color.fromString(colors.primary).toRGBA(0.15);
    derived[`--${ns}-primary-hover`] = lighten(colors.primary, 10);
    derived[`--${ns}-link`] = lighten(colors.primary, 15);
  }
  if (colors.shadow) {
    const shadow = Color.fromString(colors.shadow);
    derived[`--${ns}-shadow-color`] = shadow.toRGBA(0.4);
    derived[`--${ns}-shadow-light`] = shadow.toRGBA(0.2);
  }
  if (colors.overlay) derived[`--${ns}-overlay-color`] = Color.fromString(colors.overlay).toRGBA(0.5);
  if (colors.calloutBorder) derived[`--${ns}-callout-bg`] = Color.fromString(colors.calloutBorder).toRGBA(0.15);
  if (colors.error) derived[`--${ns}-error-bg`] = Color.fromString(colors.error).toRGBA(0.15);
  if (colors.success) derived[`--${ns}-positive-bg`] = Color.fromString(colors.success).toRGBA(0.2);
  if (colors.bgLighter) {
    const surface = Color.fromString(colors.bgLighter);
    derived[`--${ns}-surface-dim`] = colors.bgLighter;
    derived[`--${ns}-surface-dim-10`] = surface.toRGBA(0.1);
    derived[`--${ns}-surface-dim-25`] = surface.toRGBA(0.25);
    derived[`--${ns}-surface-dim-50`] = surface.toRGBA(0.5);
    derived[`--${ns}-surface-dim-75`] = surface.toRGBA(0.75);
  }
  derived[`--${ns}-text-on-color`] = '#ffffff';
  derived[`--${ns}-text-shadow-on-color`] = 'rgb(0 0 0 / 50%)';
  if (colors.bg && !colors.bgHover) derived[`--${ns}-bg-hover`] = lighten(colors.bg, 8);
  if (colors.buttonBg) derived[`--${ns}-button-hover`] = lighten(colors.buttonBg, 10);
  return derived;
}
